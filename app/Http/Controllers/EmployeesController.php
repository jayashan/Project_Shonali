     */
    public function store(Request $request)
    {
        //

        $request->validate([
            'employee_id'=>'required',
            'fname'=>'required',
            'lname'=>'required',
            'email'=>'required',
            'password'=>'required',
            'gender'=>'required',
            'address1'=>'required',
            'address2'=>'required',
        ]);

        employee::create($request->all());

        return Redirect::back()->with('message','Operation Successful !');
    }

